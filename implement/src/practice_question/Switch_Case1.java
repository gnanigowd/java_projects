package practice_question;

public class Switch_Case1 {
	public static void main(String[]args) {
		int num = 5;
		switch (num) {
			case 1:
				System.out.println("mona");
				break;
			case 2:
				System.out.println("sona");
				break;
				
			case 3 :
				System.out.println("Hina");
				break;
			case 4 :
				System.out.println("joya");
				break;
			case 5:
				System.out.println("Roja");
				break;
			
			case 6:
				System.out.println("neha");
				break;
			default :
				System.out.println("Everyone is beautiful");
			
		
		}
		
	}

}
